export function getFileIcon(fileName) {
  const name = fileName.toLowerCase();
  if (fileNames[name]) return fileNames[name];
  const parts = name.split('.');
  for (let i = 1; i < parts.length; i++) {
    const ext = parts.slice(i).join('.');
    if (fileExtensions[ext]) return fileExtensions[ext];
  }
  return "file";
}

export function getFolderIcon(folderName, isOpen = false) {
  const name = folderName.toLowerCase();
  const iconName = folderNames[name] || "folder";
  return isOpen ? iconName + "-open" : iconName;
}
